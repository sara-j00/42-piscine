void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	while (i < size)
	{
		j = i + 1;
		while (j < size - 1)
		{
			if (tab[i] > tab[j])
				tab[i] = tab[i];
			j++;
		}
		i++;
	}
}
