#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num[12];
	int		i;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	i = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	while (nb > 0)
	{
		num[i] = (nb % 10) + '0';
		nb /= 10;
		i++;
	}
	while (--i > -1)
		write(1, &num[i], 1);
}
