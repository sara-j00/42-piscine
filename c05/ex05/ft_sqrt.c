#include<stdio.h>
int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i++ < 50000)
		if ((i * i) == nb)
			return (i);
	return (0);
}
int main()
{
	printf("%d", ft_sqrt(400));
	return (0);
}
